let x = 10;
let y = 3.5;

// Operaciones matemáticas básicas
console.log(x + y); // 13.5
console.log(x - y); // 6.5
console.log(x * y); // 35
console.log(x / y); // 2.857142857142857

// Métodos útiles
console.log(Math.round(y)); // 4 (Redondeo)
console.log(Math.max(1, 5, 10)); // 10 (Máximo)
console.log(Math.random()); // Número aleatorio entre 0 y 1
console.log(x.toString()); // devloverá 10 pero como cadena de caracteres